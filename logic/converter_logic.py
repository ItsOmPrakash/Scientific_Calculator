# Length
def meter_to_cm(m):
    return m * 100

def cm_to_meter(cm):
    return cm / 100

# Temperature
def celsius_to_fahrenheit(c):
    return (c * 9 / 5) + 32

def fahrenheit_to_celsius(f):
    return (f - 32) * 5 / 9

# Speed
def kmph_to_mps(kmph):
    return kmph * 0.27778

def mps_to_kmph(mps):
    return mps * 3.6
